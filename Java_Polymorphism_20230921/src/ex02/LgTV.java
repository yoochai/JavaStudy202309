package ex02;

public class LgTV {
	
	public void turnOn() {
		System.out.println("LgTV.. 전원을 켜다");
	}
	
	public void turnOff() {
		System.out.println("LgTV.. 전원을 끄다");
	}
	
	public void soundUp() {
		System.out.println("LgTV.. 소리를 올리다");
	}
	
	public void soundDown() {
		System.out.println("LgTV.. 소리를 내리다");
	}
}
